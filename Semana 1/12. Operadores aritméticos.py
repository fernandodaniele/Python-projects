# Operadores ariméticos

# Python maneja operadores aritméticos para hacer operaciones con números, como un buen lenguaje de programación.
# Recuerden que hay dos cosas que las computadoras hacen mejor que los humanos:
## 1. Recordar cosas
## 2. Hacer operaciones rápidamente

## Por ello, es importante que conozcamos los operadores aritméticos con los que cuenta Python.

## Python maneja una sintaxis muy amigable para utilizar los operadores aritméticos. Algunos de los símbolos que
## ocuparemos te serán familiares.

## Para sumar se ocupa el símbolo de +,
## La resta se realiza con el símbolo de guión -
## Para multiplicar se utiliza el símbolo de asterisco *
## Hay dos maneras de efectuar las divisiones, ya sea con una diagonal ( / ) o con dos ( // ),
## esto depende del resultado que quieras.
## Podemos elevar un número a la "n" potencia utilizando dos asteriscos ** entre números
## Por último, también podemos obtener el residuo de una división con el operador de por ciento %

# Veamos algunos ejemplos de aplicación
# Vamos a definir dos de variables para utilizarlas en nuestro ejemplo

x = 7
y = 3

# suma (+)

z = x + y
print(z)

print(x + y + 5)

# resta (-)

print(x - y)

# multipliación (*)

z = x * y
print(z)
print (x * y)

# división (/) o (//)

z = x / y
print(z)

print(x / y)

z = x // y
print(z)

# Potenciación

z = x ** y
print(z)

z = 5 ** 3
print(z)
z = 5 * 5 * 5
print(z)

type(z)

# Residuo

z = x % y
print(z)

z = 5 % 3
print(z)